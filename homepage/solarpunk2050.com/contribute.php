    <h2>Writing own adventures</h2>
    I intentionally do not cover lots of countries and regions. So pick your home town and Solarpunkify it ! Put an adventure there and create some player characters. You can publish it on DriveThru or other platforms under your name (with a link to Solarpunk 2050).

    <h2>Contribution guide</h2>
    Contact me first ! That way I can weave your ideas into the world bible and link to your contribution. I will also have some basic concepts ready for you so you do not break the world canon by accident.

    <h2>Solarpunk 2035 and Solarpunk 2070</h2>
    If you want to build a big setting and can propose those 2 time windows. Solarpunk 2050 is set in the revolution phase. Many Solarpunk islands are visible but there is still lots of work to do.

    I will leave the other two time windows open as todo for whoever wants to tackle them.

    <h3>Solarpunk 2035</h3>
    Would be set in the grim and dark phase before. Small glimmers of hope will start the transition to the revolution phase of Solarpunk 2050

    <h3>Solarpunk 2070</h3>
    Is the phase where Solarpunk is globally established and everything is shiny...except some new types of problems and challenges that need protagonists to solve them.



